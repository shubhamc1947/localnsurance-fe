// ─── Helper: ISO 2-letter country code → flag emoji ───────────────────────────
export function countryFlag(code: string): string {
  return code
    .toUpperCase()
    .replace(/./g, (c) => String.fromCodePoint(0x1f1e6 - 65 + c.charCodeAt(0)));
}
